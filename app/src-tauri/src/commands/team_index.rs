    #[test]
    fn people_aggregation_counts_actors_and_top_tags() {
        let evs = vec![
            ev_with_refs(
                "evt-2026-04-29-aaa1112222",
                "2026-04-29T11:30:00Z",
                "daizhe",
                "meeting_chunk",
                "x",
                serde_json::json!({ "tags": ["pcb", "gtm"] }),
                "cursor",
            ),
            ev_with_refs(
                "evt-2026-04-29-bbb1112222",
                "2026-04-29T11:00:00Z",
                "daizhe",
                "meeting_chunk",
                "y",
                serde_json::json!({ "tags": ["pcb"] }),
                "claude_code",
            ),
            // Stale (older than 24h) — excluded.
            ev(
                "evt-2026-04-27-ccc1112222",
                "2026-04-27T11:00:00Z",
                "stale",
                "meeting_chunk",
                "z",
            ),
        ];
        let body = build_team_index_markdown(&evs, now());
        assert!(body.contains("daizhe"));
        assert!(body.contains("cursor"));
        assert!(body.contains("claude_code"));
        assert!(body.contains("2 atoms"));
        // pcb appears in 2 atoms → first in top-tags ranking.
        assert!(body.contains("top tags: pcb, gtm"));
        assert!(!body.contains("- stale "));
    }

    #[test]
    fn recent_atoms_truncates_body_to_80_chars() {
        let long =
            "this is a very long body line that absolutely should be truncated to fit in the recent atoms section because we cap at eighty characters";
        let evs = vec![ev(
            "evt-2026-04-29-aaa1112222",
            "2026-04-29T11:55:00Z",
            "daizhe",
            "meeting_chunk",
            long,
        )];
        let body = build_team_index_markdown(&evs, now());
        // The trimmed body in "Recent atoms" line cannot exceed ~80 chars
        // followed by ellipsis. A simple invariant: every line in the
        // file is shorter than the full original body.
        for line in body.lines().filter(|l| l.starts_with("- 11:55 daizhe")) {
            assert!(line.contains("…"));
            assert!(line.len() < long.len() + 40);
        }
    }

    #[test]
    fn ten_kb_cap_truncates_recent_atoms_first() {
        // Generate 200 atoms with long bodies to bust the 10 KB cap.
        let mut evs: Vec<TimelineEvent> = Vec::new();
        for i in 0..200u32 {
            let body = format!("decision body number {i} with a moderate amount of explanatory text");
            evs.push(ev(
                &format!("evt-2026-04-29-{:010x}", i),
                "2026-04-29T11:55:00Z",
                "daizhe",
                "meeting_chunk",
                &body,
            ));
        }
        let body = build_team_index_markdown(&evs, now());
        assert!(
            body.len() <= HARD_CEILING_BYTES,
            "body length {} exceeded ceiling {}",
            body.len(),
            HARD_CEILING_BYTES
        );
        // The header sections must still be intact.
        assert!(body.contains("Recent decisions"));
        assert!(body.contains("Active threads"));
        assert!(body.contains("Who's been active"));
    }

    #[test]
    fn write_team_index_to_round_trips_disk() {
        let dir = std::env::temp_dir().join(format!(
            "tg_team_index_{}",
            uuid::Uuid::new_v4().simple()
        ));
        std::fs::create_dir_all(dir.join(".tangerine")).unwrap();
        // Write a 1-event timeline.json so load_all_events returns 1.
        let payload = serde_json::json!({
            "version": 1,
            "events": [{
                "id": "evt-2026-04-29-aaa1112222",
                "ts": "2026-04-29T11:55:00Z",
                "source": "claude_code",
                "actor": "daizhe",
                "actors": ["daizhe"],
                "kind": "decision",
                "refs": { "tags": ["pcb"] },
                "status": "active",
                "body": "Pin gateway to RK3588"
            }],
            "vector_store": { "type": "none", "dimensions": null, "model": null }
        });
        std::fs::write(
            dir.join(".tangerine").join("timeline.json"),
            serde_json::to_string(&payload).unwrap(),
        )
        .unwrap();

        let out = write_team_index_to(&dir).expect("write succeeded");
        assert_eq!(out.atoms_scanned, 1);
        assert!(out.bytes_written > 0);
        let written = std::fs::read_to_string(dir.join("TEAM_INDEX.md")).unwrap();
        assert!(written.starts_with(SENTINEL));
        assert!(written.contains("Pin gateway to RK3588"));
        assert!(written.contains("[#pcb]"));
        let _ = std::fs::remove_dir_all(&dir);
    }

