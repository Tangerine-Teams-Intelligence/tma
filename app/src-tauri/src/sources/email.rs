// Atom write — frontmatter + body assembly

/// Build the full markdown body for a thread atom. Pure function — tests
/// assert on this directly without touching the filesystem.
pub fn build_email_atom(thread: &EmailThread, provider: &str) -> String {
    let participants = thread.participants();
    let last = thread.last_message_at().unwrap_or("");
    let subject = thread
        .messages
        .first()
        .map(|m| m.subject.clone())
        .unwrap_or_else(|| thread.subject_root.clone());
    let mut message_ids: Vec<String> = thread
        .messages
        .iter()
        .map(|m| m.message_id.clone())
        .filter(|id| !id.is_empty())
        .collect();
    message_ids.sort();
    message_ids.dedup();

    let mut out = String::new();
    out.push_str("---\n");
    out.push_str(&format!("source: email\n"));
    out.push_str(&format!("provider: {}\n", yaml_scalar(provider)));
    out.push_str(&format!("subject: {}\n", yaml_scalar(&subject)));
    out.push_str("participants:\n");
    for p in &participants {
        out.push_str(&format!("  - {}\n", yaml_scalar(p)));
    }
    out.push_str(&format!("last_message_at: {}\n", yaml_scalar(last)));
    out.push_str("message_ids:\n");
    for id in &message_ids {
        out.push_str(&format!("  - {}\n", yaml_scalar(id)));
    }
    out.push_str("---\n\n");

    out.push_str(&format!("# {}\n\n", subject));
    for m in &thread.messages {
        out.push_str(&format!(
            "## {} — {}\n\n",
            if m.from.is_empty() { "(unknown)" } else { &m.from },
            if m.date.is_empty() { "—" } else { &m.date },
        ));
        let truncated: String = m.body.chars().take(800).collect();
        out.push_str(truncated.trim());
        out.push_str("\n\n");
    }
    out
}

/// Quote a YAML scalar value safely. We intentionally keep this minimal —
/// the schema is small and the strings are constrained (no embedded
/// newlines after our own normalisation).
fn yaml_scalar(s: &str) -> String {
    let needs_quote = s.is_empty()
        || s.contains(':')
        || s.contains('#')
        || s.contains('\n')
        || s.starts_with('-')
        || s.starts_with(' ')
        || s.ends_with(' ');
    if needs_quote {
        let escaped = s.replace('\\', "\\\\").replace('"', "\\\"").replace('\n', "\\n");
        format!("\"{}\"", escaped)
    } else {
        s.to_string()
    }
}

/// Read the existing atom (if present) and return the set of `message_ids`
/// already recorded. Used for dedup so re-fetching doesn't bloat the atom.
pub fn read_existing_message_ids(path: &Path) -> BTreeSet<String> {
    let mut out = BTreeSet::new();
    let raw = match std::fs::read_to_string(path) {
        Ok(s) => s,
        Err(_) => return out,
    };
    // Find the message_ids: section in the frontmatter.
    let mut in_block = false;
    let mut in_ids = false;
    for line in raw.lines() {
        if line.trim() == "---" {
            in_block = !in_block;
            if !in_block {
                break;
            }
            continue;
        }
        if !in_block {
            continue;
        }
        if line.trim_start().starts_with("message_ids:") {
            in_ids = true;
            continue;
        }
        if in_ids {
            if let Some(rest) = line.trim_start().strip_prefix("- ") {
                let id = rest.trim().trim_matches('"').trim_matches('\'').to_string();
                if !id.is_empty() {
                    out.insert(id);
                }
                continue;
            }
            // Hit the next key — done.
            if line.starts_with(|c: char| !c.is_whitespace()) {
                in_ids = false;
            }
        }
    }
    out
}

