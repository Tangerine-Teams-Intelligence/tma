// IMAP fetch — generic over the stream so tests can stub.

/// Minimal IMAP surface we depend on. Lets `fetch_recent_with_stream` take
/// either a real `imap::Session` (production) or a `FakeImapStream` (tests).
pub trait ImapStream {
    /// Select the INBOX. Returns the highest UID currently in the box.
    fn select_inbox(&mut self) -> Result<u32, String>;
    /// Fetch raw RFC 822 bodies for messages whose internal date is on or
    /// after `since`. Each entry is `(uid, body_bytes)`.
    fn fetch_since(&mut self, since: DateTime<Utc>) -> Result<Vec<(u32, Vec<u8>)>, String>;
    /// Close the session. Best-effort.
    fn logout(&mut self);
}

/// Parse one RFC 822 body into our `EmailMessage`. Tolerant — bad dates,
/// missing fields, or mailparse errors fall back to safe defaults so the
/// thread group still lands.
pub fn parse_rfc822(raw: &[u8]) -> Result<EmailMessage, String> {
    let parsed = mailparse::parse_mail(raw).map_err(|e| format!("parse: {e}"))?;
    let headers = &parsed.headers;
    let lookup = |key: &str| {
        headers
            .iter()
            .find(|h| h.get_key().eq_ignore_ascii_case(key))
            .map(|h| h.get_value())
            .unwrap_or_default()
    };
    let message_id = lookup("Message-ID")
        .trim()
        .trim_start_matches('<')
        .trim_end_matches('>')
        .to_string();
    let subject = lookup("Subject");
    let from = lookup("From");
    let to: Vec<String> = lookup("To")
        .split(',')
        .map(|s| s.trim().to_string())
        .filter(|s| !s.is_empty())
        .collect();
    let cc: Vec<String> = lookup("Cc")
        .split(',')
        .map(|s| s.trim().to_string())
        .filter(|s| !s.is_empty())
        .collect();
    let date_raw = lookup("Date");
    let date = mailparse::dateparse(&date_raw)
        .ok()
        .and_then(|secs| chrono::DateTime::<Utc>::from_timestamp(secs, 0))
        .map(|dt| dt.to_rfc3339())
        .unwrap_or_else(|| Utc::now().to_rfc3339());

    let mut references: Vec<String> = Vec::new();
    for (key, _) in [("In-Reply-To", ()), ("References", ())] {
        let raw = lookup(key);
        for tok in raw.split_whitespace() {
            let cleaned = tok.trim().trim_start_matches('<').trim_end_matches('>');
            if !cleaned.is_empty() {
                references.push(cleaned.to_string());
            }
        }
    }
    references.sort();
    references.dedup();

    let body = first_text_body(&parsed).unwrap_or_default();

    Ok(EmailMessage {
        message_id,
        subject,
        from,
        to,
        cc,
        date,
        body,
        references,
    })
}

/// Pull the first text/plain body (fall back to text/html stripped to
/// plain). Recurses through MIME parts.
fn first_text_body(part: &mailparse::ParsedMail) -> Option<String> {
    let ctype = part.ctype.mimetype.to_lowercase();
    if ctype == "text/plain" {
        return part.get_body().ok();
    }
    if ctype == "text/html" {
        // Cheap HTML strip — drop tags. Good enough for digest.
        let html = part.get_body().ok()?;
        return Some(strip_html(&html));
    }
    for sub in &part.subparts {
        if let Some(body) = first_text_body(sub) {
            return Some(body);
        }
    }
    None
}

fn strip_html(s: &str) -> String {
    let mut out = String::with_capacity(s.len());
    let mut in_tag = false;
    for c in s.chars() {
        match c {
            '<' => in_tag = true,
            '>' => in_tag = false,
            _ if !in_tag => out.push(c),
            _ => {}
        }
    }
    out.split_whitespace().collect::<Vec<_>>().join(" ")
}

/// Group a flat list of messages into threads by normalised subject. We
/// also unify threads via the `references` chain — if message B has a
/// reference to message A's Message-ID, B joins A's group regardless of
/// subject (handles the "subject changed mid-thread" case).
pub fn thread_messages(messages: Vec<EmailMessage>) -> Vec<EmailThread> {
    let mut by_subject: BTreeMap<String, EmailThread> = BTreeMap::new();
    let mut id_to_subject: BTreeMap<String, String> = BTreeMap::new();
    // First pass — group by subject root.
    for m in &messages {
        let root = normalise_subject(&m.subject);
        if !m.message_id.is_empty() {
            id_to_subject.insert(m.message_id.clone(), root.clone());
        }
        let entry = by_subject
            .entry(root.clone())
            .or_insert_with(|| EmailThread {
                subject_root: root.clone(),
                messages: Vec::new(),
            });
        entry.messages.push(m.clone());
    }
    // Second pass — for each message, if any of its `references` points
    // at a Message-ID in a different subject group, merge the smaller
    // group into the larger one.
    let mut merge_into: BTreeMap<String, String> = BTreeMap::new();
    for m in &messages {
        let own_root = normalise_subject(&m.subject);
        for r in &m.references {
            if let Some(other_root) = id_to_subject.get(r) {
                if other_root != &own_root {
                    let (winner, loser) = if other_root < &own_root {
                        (other_root.clone(), own_root.clone())
                    } else {
                        (own_root.clone(), other_root.clone())
                    };
                    merge_into.insert(loser, winner);
                }
            }
        }
    }
    // Resolve transitive merges (loser → winner → ...).
    let mut resolved: BTreeMap<String, String> = BTreeMap::new();
    for (loser, _) in merge_into.iter() {
        let mut cur = loser.clone();
        let mut seen = BTreeSet::new();
        while let Some(next) = merge_into.get(&cur) {
            if !seen.insert(cur.clone()) {
                break;
            }
            cur = next.clone();
        }
        resolved.insert(loser.clone(), cur);
    }
    for (loser, winner) in resolved {
        if let Some(thread) = by_subject.remove(&loser) {
            if let Some(target) = by_subject.get_mut(&winner) {
                target.messages.extend(thread.messages);
            } else {
                // Winner gone — keep the loser group intact under its own key.
                by_subject.insert(loser, thread);
            }
        }
    }
    // Sort messages inside each thread by date (best-effort string compare;
    // RFC 3339 sorts lexicographically when timezone-normalised, which our
    // parser ensures).
    let mut threads: Vec<EmailThread> = by_subject.into_values().collect();
    for t in &mut threads {
        t.messages.sort_by(|a, b| a.date.cmp(&b.date));
    }
    threads
}

