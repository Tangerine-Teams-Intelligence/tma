// IMAP transport — production. Hidden behind `cfg(not(test))` no-op stubs to
// keep the test surface deterministic; the real wiring uses `imap::Client`.

#[cfg(not(test))]
fn connect_imap_blocking(
    host: &str,
    port: u16,
    username: &str,
    password: &str,
) -> Result<imap::Session<imap::Connection>, String> {
    let client = imap::ClientBuilder::new(host, port)
        .connect()
        .map_err(|e| format!("connect: {e}"))?;
    let session = client
        .login(username, password)
        .map_err(|(e, _)| format!("login: {e}"))?;
    Ok(session)
}

#[cfg(not(test))]
fn fetch_recent_via_imap(
    cfg: &EmailConfig,
    password: &str,
    since: DateTime<Utc>,
) -> Result<Vec<(u32, Vec<u8>)>, String> {
    let (host, port) = cfg.imap_endpoint().map_err(|e| e.to_string())?;
    let mut session = connect_imap_blocking(&host, port, &cfg.username, password)?;
    session
        .select("INBOX")
        .map_err(|e| format!("select inbox: {e}"))?;
    // IMAP SINCE date format: `DD-Mon-YYYY` per RFC 3501.
    let since_str = since.format("%d-%b-%Y").to_string();
    let uids = session
        .uid_search(format!("SINCE {}", since_str))
        .map_err(|e| format!("search: {e}"))?;
    if uids.is_empty() {
        let _ = session.logout();
        return Ok(vec![]);
    }
    let uid_set = uids
        .iter()
        .map(|u| u.to_string())
        .collect::<Vec<_>>()
        .join(",");
    let messages = session
        .uid_fetch(&uid_set, "RFC822")
        .map_err(|e| format!("fetch: {e}"))?;
    let mut out = Vec::new();
    for msg in messages.iter() {
        if let (Some(uid), Some(body)) = (msg.uid, msg.body()) {
            out.push((uid, body.to_vec()));
        }
    }
    let _ = session.logout();
    Ok(out)
}

