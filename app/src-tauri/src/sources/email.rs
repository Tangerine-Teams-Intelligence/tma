#[derive(Debug, Deserialize)]
pub struct EmailTestConnectionArgs {
    pub config: EmailConfig,
}

#[derive(Debug, Serialize)]
pub struct EmailTestConnectionResult {
    pub ok: bool,
    pub host: String,
    pub port: u16,
    pub error: Option<String>,
    /// True when the password got stored. False on dry-run (no password).
    pub stored_password: bool,
}

/// Tests the IMAP login. On success, persists the app password to the
/// keychain so subsequent fetches use the stored credential.
#[tauri::command(rename_all = "snake_case")]
pub async fn email_test_connection(
    _state: State<'_, AppState>,
    args: EmailTestConnectionArgs,
) -> Result<EmailTestConnectionResult, AppError> {
    let cfg = args.config;
    if cfg.username.trim().is_empty() {
        return Ok(EmailTestConnectionResult {
            ok: false,
            host: String::new(),
            port: 0,
            error: Some("username is empty".into()),
            stored_password: false,
        });
    }
    let (host, port) = cfg.imap_endpoint()?;
    let password = match cfg.app_password.as_deref() {
        Some(p) if !p.is_empty() => p.to_string(),
        _ => {
            return Ok(EmailTestConnectionResult {
                ok: false,
                host,
                port,
                error: Some("app password is empty".into()),
                stored_password: false,
            });
        }
    };
    // Try a real login. Wrapped in spawn_blocking to keep the runtime free.
    #[cfg(not(test))]
    {
        let host_c = host.clone();
        let user = cfg.username.clone();
        let pwd = password.clone();
        let join = tokio::task::spawn_blocking(move || {
            connect_imap_blocking(&host_c, port, &user, &pwd).map(|mut s| {
                let _ = s.logout();
            })
        })
        .await
        .map_err(|e| AppError::internal("imap_join", e.to_string()))?;
        if let Err(e) = join {
            return Ok(EmailTestConnectionResult {
                ok: false,
                host,
                port,
                error: Some(e),
                stored_password: false,
            });
        }
    }
    // Persist the password on success.
    TokenStore::set(&cfg.keyring_login(), &password)?;
    Ok(EmailTestConnectionResult {
        ok: true,
        host,
        port,
        error: None,
        stored_password: true,
    })
}

