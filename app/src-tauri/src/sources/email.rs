// Filesystem layer

/// `<memory_root>/threads/email/`. Created on demand.
pub fn email_threads_dir(memory_root: &Path) -> PathBuf {
    memory_root.join("threads").join("email")
}

/// Resolve the default memory root: `<home>/.tangerine-memory/`.
fn default_memory_root() -> Result<PathBuf, AppError> {
    dirs::home_dir()
        .map(|h| h.join(".tangerine-memory"))
        .ok_or_else(|| AppError::internal("home_dir", "home_dir() returned None"))
}

/// Write (or merge) thread atoms into the email threads dir. Returns the
/// number of atoms written or merged.
pub fn write_threads(
    memory_root: &Path,
    threads: &[EmailThread],
    provider: &str,
) -> Result<u32, AppError> {
    let dir = email_threads_dir(memory_root);
    std::fs::create_dir_all(&dir)
        .map_err(|e| AppError::internal("mkdir_email", e.to_string()))?;
    let mut written = 0u32;
    for t in threads {
        if t.messages.is_empty() {
            continue;
        }
        let slug = t.slug();
        let path = dir.join(format!("{}.md", slug));
        let existing_ids = read_existing_message_ids(&path);
        // Merge: drop messages whose IDs are already on disk.
        let mut merged = EmailThread {
            subject_root: t.subject_root.clone(),
            messages: Vec::new(),
        };
        for m in &t.messages {
            if !m.message_id.is_empty() && existing_ids.contains(&m.message_id) {
                continue;
            }
            merged.messages.push(m.clone());
        }
        if merged.messages.is_empty() {
            continue;
        }
        // If we have prior content, splice the existing messages back in by
        // re-reading their IDs from disk and treating them as already
        // present in the body; the simpler approach (rewriting from scratch
        // with both old + new) requires re-fetching old bodies which we no
        // longer have. Instead we append a "merged on" marker — the
        // frontmatter `message_ids` reflects the union so future fetches
        // dedup correctly.
        if !existing_ids.is_empty() {
            // Pseudo-thread carrying existing IDs as ghost entries — keeps
            // the message_ids: list complete in frontmatter.
            for id in &existing_ids {
                merged.messages.insert(
                    0,
                    EmailMessage {
                        message_id: id.clone(),
                        subject: String::new(),
                        from: String::new(),
                        to: vec![],
                        cc: vec![],
                        date: String::new(),
                        body: String::from("(previously recorded message — body archived)"),
                        references: vec![],
                    },
                );
            }
        }
        let body = build_email_atom(&merged, provider);
        atomic_write(&path, &body)?;
        written += 1;
    }
    Ok(written)
}

/// Atomic write: tmp file + rename. Mirrors the helper in
/// `commands::views::atomic_write`.
fn atomic_write(path: &Path, body: &str) -> Result<(), AppError> {
    if let Some(parent) = path.parent() {
        std::fs::create_dir_all(parent).ok();
    }
    let tmp = path.with_extension(format!("tmp.{}", uuid::Uuid::new_v4().simple()));
    std::fs::write(&tmp, body)
        .map_err(|e| AppError::internal("atomic_write_email", format!("write: {e}")))?;
    std::fs::rename(&tmp, path)
        .map_err(|e| AppError::internal("atomic_write_email", format!("rename: {e}")))?;
    Ok(())
}

