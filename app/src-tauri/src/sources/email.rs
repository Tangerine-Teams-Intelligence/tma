#[derive(Debug, Clone, Serialize)]
pub struct FetchResult {
    pub threads_written: u32,
    pub messages_seen: u32,
    pub provider: String,
}

/// Fetch recent messages, group, and write atoms. Reads the password from
/// `TokenStore` keyed on `cfg.keyring_login()`. Memory root defaults to
/// `<home>/.tangerine-memory/` unless `memory_root_override` is given.
pub async fn fetch_recent(
    cfg: EmailConfig,
    memory_root_override: Option<PathBuf>,
) -> Result<FetchResult, AppError> {
    if cfg.fetch_lookback_days == 0 || cfg.fetch_lookback_days > 30 {
        return Err(AppError::user(
            "lookback_oob",
            "fetch_lookback_days must be in 1..=30",
        ));
    }
    let memory_root = match memory_root_override {
        Some(p) => p,
        None => default_memory_root()?,
    };
    let password = TokenStore::get(&cfg.keyring_login()).map_err(|_| {
        AppError::user(
            "email_password_missing",
            "no app password stored — run /sources/email setup first",
        )
    })?;
    let since = Utc::now() - chrono::Duration::days(cfg.fetch_lookback_days as i64);

    let raw = fetch_via_blocking(&cfg, &password, since).await?;
    let messages_seen = raw.len() as u32;
    let mut messages: Vec<EmailMessage> = Vec::new();
    for (_uid, body) in raw {
        match parse_rfc822(&body) {
            Ok(m) => messages.push(m),
            Err(e) => tracing::warn!(error = %e, "email: parse_rfc822 failed; skipping message"),
        }
    }
    let threads = thread_messages(messages);
    let written = write_threads(&memory_root, &threads, &cfg.provider)?;
    Ok(FetchResult {
        threads_written: written,
        messages_seen,
        provider: cfg.provider,
    })
}

#[cfg(not(test))]
async fn fetch_via_blocking(
    cfg: &EmailConfig,
    password: &str,
    since: DateTime<Utc>,
) -> Result<Vec<(u32, Vec<u8>)>, AppError> {
    let cfg = cfg.clone();
    let password = password.to_string();
    let join = tokio::task::spawn_blocking(move || fetch_recent_via_imap(&cfg, &password, since))
        .await
        .map_err(|e| AppError::internal("imap_join", e.to_string()))?;
    join.map_err(|e| AppError::external("imap", e))
}

