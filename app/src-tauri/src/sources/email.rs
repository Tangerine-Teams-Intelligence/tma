//! v1.8 Phase 2-D — Email source (IMAP digest).
//!
//! Pulls recent inbox messages from Gmail / Outlook over IMAPS (993),
//! groups them by thread (normalised subject + reply chain), and writes
//! one atom per thread to `~/.tangerine-memory/threads/email/`.
//!
//! Design notes
//! ============
//!
//! * **Auth**: app password (Gmail / Outlook MS-account "App password" UI).
//!   The `EmailConfig.app_password` arrives over IPC, gets stored in the
//!   OS keychain via `commands::sync::TokenStore` keyed on the username.
//!   We never round-trip the secret to the React layer — the frontend only
//!   sends it once, then references the username. Same model as the
//!   GitHub OAuth token store.
//!
//! * **IMAP crate**: `imap` 3.0 with `native-tls`. Blocking interface
//!   wrapped in `tokio::task::spawn_blocking` so the runtime stays free
//!   for IPC. No async-imap — the blocking surface is well-trodden, the
//!   per-fetch overhead is negligible compared to the IMAP server's own
//!   round-trip latency.
//!
//! * **Threading**: we group by `subject_root` (subject with leading
//!   `Re:` / `Fwd:` / `回复:` / `[Tag]` prefixes stripped, lowercased) plus
//!   the In-Reply-To / References chain. Two messages whose normalised
//!   subjects match OR whose Message-IDs link via References are placed
//!   in the same thread. This is an approximation of Gmail's algorithm —
//!   good enough for the digest; the user can split threads manually by
//!   editing the atom file.
//!
//! * **Atom shape**: `~/.tangerine-memory/threads/email/{thread-slug}.md`.
//!   Slug = first 40 chars of the subject root, slugified. Frontmatter:
//!   `subject`, `participants`, `last_message_at`, `source: email`,
//!   `message_ids`, `provider`. Body = chronological message digest with
//!   one paragraph per message (sender, ts, first 800 chars of body).
//!
//! * **Dedup**: rerunning `fetch_recent` doesn't write duplicates. We
//!   merge new messages into the existing atom by reading its
//!   `message_ids:` frontmatter line and skipping IDs we've already
//!   recorded. The fetch tracks per-thread dirty bits so we only rewrite
//!   atoms whose content actually changed.
//!
//! * **Tests**: an `ImapStream` trait abstraction lets unit tests stub the
//!   network. Real production uses `imap::Session<TlsStream<TcpStream>>`;
//!   tests use a `FakeImapStream` that yields canned message bodies.

