// Public config shape — mirrored on the React side.

/// Provider tag drives the IMAP host + port defaults. We keep the shape open
/// (string, not enum) so a future "imap" / "fastmail" / "yahoo" entry doesn't
/// require a Rust schema bump.
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq, Eq)]
pub struct EmailConfig {
    /// "gmail" | "outlook" | "imap" (custom). Anything else is rejected.
    pub provider: String,
    /// Email address — also doubles as the IMAP login on both Gmail and
    /// Outlook.
    pub username: String,
    /// App password (NOT the user's primary password). Generated by the
    /// user from their Google / Microsoft account settings. Stored via
    /// `TokenStore` keyed on `username`; not echoed back over IPC after
    /// the initial set.
    #[serde(default)]
    pub app_password: Option<String>,
    /// How far back to fetch. Hard-cap at 30 to keep first-run wait time
    /// bounded; the daemon's daily heartbeat means a missed window
    /// catches up within 24 h regardless.
    #[serde(default = "default_lookback_days")]
    pub fetch_lookback_days: u32,
    /// Optional override — when set, skips the provider lookup. Used for
    /// custom IMAP servers and for tests.
    #[serde(default)]
    pub host: Option<String>,
    #[serde(default)]
    pub port: Option<u16>,
}

fn default_lookback_days() -> u32 {
    7
}

impl EmailConfig {
    /// Resolve `(host, port)` for the given provider. Rejects unknown
    /// provider strings unless `host` is explicitly set.
    pub fn imap_endpoint(&self) -> Result<(String, u16), AppError> {
        if let (Some(h), Some(p)) = (&self.host, self.port) {
            return Ok((h.clone(), p));
        }
        match self.provider.as_str() {
            "gmail" => Ok(("imap.gmail.com".to_string(), 993)),
            "outlook" => Ok(("outlook.office365.com".to_string(), 993)),
            "imap" => self
                .host
                .clone()
                .map(|h| (h, self.port.unwrap_or(993)))
                .ok_or_else(|| {
                    AppError::user(
                        "imap_host_missing",
                        "host is required when provider == 'imap'",
                    )
                }),
            other => Err(AppError::user(
                "unknown_provider",
                format!("unknown email provider: {other}"),
            )),
        }
    }

    pub fn keyring_login(&self) -> String {
        format!("email:{}", self.username)
    }
}

// ---------------------------------------------------------------------------
// Atom shape

/// One message lifted from IMAP. Crate-internal — flattened into the atom
/// body when we write to disk.
#[derive(Debug, Clone, Serialize, Deserialize, PartialEq, Eq)]
pub struct EmailMessage {
    pub message_id: String,
    pub subject: String,
    pub from: String,
    pub to: Vec<String>,
    pub cc: Vec<String>,
    /// RFC 3339. We accept whatever mailparse hands back; bad dates fall
    /// through to "now" so the atom still gets a sortable timestamp.
    pub date: String,
    pub body: String,
    /// Parsed `In-Reply-To` + `References` headers, deduped. Used for
    /// threading.
    pub references: Vec<String>,
}

/// Thread = one or more messages sharing a subject root (or chained via
/// References). The atom file is named after the thread.
#[derive(Debug, Clone, Serialize, Deserialize, Default, PartialEq, Eq)]
pub struct EmailThread {
    pub subject_root: String,
    pub messages: Vec<EmailMessage>,
}

impl EmailThread {
    pub fn participants(&self) -> Vec<String> {
        let mut set: BTreeSet<String> = BTreeSet::new();
        for m in &self.messages {
            if !m.from.is_empty() {
                set.insert(m.from.clone());
            }
            for t in &m.to {
                set.insert(t.clone());
            }
            for c in &m.cc {
                set.insert(c.clone());
            }
        }
        set.into_iter().collect()
    }

    pub fn last_message_at(&self) -> Option<&str> {
        self.messages.last().map(|m| m.date.as_str())
    }

    pub fn slug(&self) -> String {
        slugify_subject(&self.subject_root)
    }
}

// ---------------------------------------------------------------------------
// Subject normalisation + slugification

/// Strip leading reply / forward markers and bracketed tags so two messages
/// in the same thread land on the same key. Lowercases the result.
///
/// Examples:
///   "Re: [Eng] OKRs"     -> "okrs"
///   "Fwd: Re: meeting"   -> "meeting"
///   "回复: 项目计划"      -> "项目计划"
pub fn normalise_subject(raw: &str) -> String {
    let mut s = raw.trim().to_string();
    let prefixes = [
        "re:", "fw:", "fwd:", "回复:", "回复：", "答复:", "答复：",
        "antwort:", "rép:", "ответ:",
    ];
    let mut changed = true;
    while changed {
        changed = false;
        let lower = s.to_lowercase();
        for p in prefixes {
            if let Some(rest) = lower.strip_prefix(p) {
                let cut = s.len() - rest.len();
                s = s[cut..].trim_start().to_string();
                changed = true;
                break;
            }
        }
        // Strip a single leading `[bracket tag]` group.
        if s.starts_with('[') {
            if let Some(end) = s.find(']') {
                if end + 1 < s.len() {
                    s = s[end + 1..].trim_start().to_string();
                    changed = true;
                }
            }
        }
    }
    s.trim().to_lowercase()
}

/// Ascii-fold + lowercase + dash. Capped at 40 visible chars so the file
/// name stays human-readable on Windows (which doesn't love 200-char
/// paths).
pub fn slugify_subject(s: &str) -> String {
    let mut out = String::with_capacity(s.len());
    let mut last_dash = true;
    for c in s.chars() {
        if c.is_ascii_alphanumeric() {
            out.push(c.to_ascii_lowercase());
            last_dash = false;
        } else if c.is_alphanumeric() {
            // Keep non-ASCII letters (Chinese, etc.) verbatim — Windows
            // handles UTF-8 file names since 1909+.
            out.push(c);
            last_dash = false;
        } else if !last_dash {
            out.push('-');
            last_dash = true;
        }
    }
    let trimmed = out.trim_matches('-').to_string();
    if trimmed.is_empty() {
        return "untitled".to_string();
    }
    // Cap at 40 chars so the resulting filename + ".md" stays readable.
    let mut buf = String::new();
    let mut count = 0;
    for c in trimmed.chars() {
        if count >= 40 {
            break;
        }
        buf.push(c);
        count += 1;
    }
    buf.trim_end_matches('-').to_string()
}

